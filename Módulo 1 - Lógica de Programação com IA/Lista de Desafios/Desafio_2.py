"""2. Peça ao usuário para inserir uma base e um expoente, e calcule a potência da base elevada ao expoente. """

base = float(input("Insira o valor da base: "))
expoente = int(input("Agora insira um valor inteiro para definir o expoente: "))

print(f"A potência da base {base}, elevado ao expoente {expoente} é: {base ** expoente} ")