# Função 1
# Para definir uma função, você irá utilizar o "def"
# depois definir o nome da função e os seus parâmetros entre parenteses

def saudacao(nome):
    print(f'Olá {nome} seja bem vindo.')

# Chamando Função (1)
saudacao('Gabriel')

# Chamando Função (2)
nome = input('Digite seu nome: ')
saudacao(nome)