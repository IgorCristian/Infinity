# Ex01. Faça uma função "multiplicar" que recebe dois números e retorna o produto entre eles.

# Definição
def multiplicar(n1,n2):
    return n1 * n2


# Código Principal - Utilizado
n1 = int(input("Digite um número: "))
n2 = int(input("Digite um número para multiplicar: "))

print(f'O produto é {multiplicar(n1,n2)}.' )