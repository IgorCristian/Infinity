# Função 2
# Para retornar valores dentro de uma função
# basta utilizar a paçavra reservada "return"
def somar(n1, n2):
    return n1 + n2


# Na chamada da função, após a sua execução
# o retorno assume o lugar da função
resultado = somar(4, 9)
print(resultado)

# Printando o retorno da função
print(somar(4,12))